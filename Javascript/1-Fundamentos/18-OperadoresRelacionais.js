// Operadores relacionais comparam os valores 
//eles sempre retornam os valores: Verdadeiro ou Falso

//maior
console.log(5 > 3) //true

//menor
console.log(5 < 3) //false

//maior ou igual
console.log(5 >= 3) //true

//menor ou igual
console.log(5 <= 3) //false

//igual
console.log(5 == 5)//true

//exatamente igual
console.log("5" === 5)//false
//para esse operador os dois devem possuir o mesmo valor e tipo

//diferente
console.log(5 != "5")//false

//extritamente diferente
console.log(1 !== "1")//true