//Um objeto em JS é declarado por {}(chaves)
const prod1 = {}

//Um objeto é uma coleção de chave e valor
//no caso nome é a chave
// e tiago é o valor
prod1.nome = "tiago"
//assim fazemos para adicionar valores nos obj de forma dinamica


//outra forma de declarar obj
const prod2 = {
    nome: "dani",
    idade: 26
}


console.log(prod1.nome)//tiago
console.log(prod2)//{ nome: 'dani', idade: 26 }