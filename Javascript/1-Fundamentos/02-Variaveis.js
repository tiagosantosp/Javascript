/* Variável é um indentificador para um dado 
    No JS não é necessário declarar o tipo da variável */

//Para declarar podemos utilizar o nome VAR, LET e CONST
var a = "TIAGO"
let b = "DANI"
const c = "MARIA"

// a variável "A"  recebeu o valor "TIAGO"
// a variável "B"  recebeu o valor "DANI"

/* a constante "C"  recebeu o valor "MARIA"
como nome já diz constante não pode ser alterada*/

console.log(a)
console.log(b)
console.log(c)

/* Pelo JS ser uma linguagem fracamente tipada podemos
utilizar outros tipos de dados nas mesmas variáveis*/

