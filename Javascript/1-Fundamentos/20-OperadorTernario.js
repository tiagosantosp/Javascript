/*Operador ternário serve para comparar algo e retornar determinado
valor dependendo do resultado*/

//a estrutura funciona assim 
//operação logica ? resultado verdadeiro : resultado falso

//EXEMPLOS

7 > 4 ? console.log("SIM") : console.log("NÂO")//SIM

1 > 4 ? console.log("SIM") : console.log("NÂO")//NÃO

0 == 4 ? console.log("SIM") : console.log("NÂO")//NÂO

0 != 4 ? console.log("SIM") : console.log("NÂO")//SIM


