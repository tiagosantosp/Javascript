let numero = 0
/*no codigo enquanto numero for menor que 5 faça
ele vai executar o que está dentro do bloco até 
numero ser maior que 5*/
while (numero < 5){
    console.log("OI")
    //por isso colocamos um incremento em numero
    //caso contrario viraria um looping infinito    
    numero++
}